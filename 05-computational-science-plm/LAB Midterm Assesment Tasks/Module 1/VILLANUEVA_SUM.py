# Prompts the user to enter three integer-point values 
# and stores them in variables called num1, num2, and num3
num1 = int(input("Enter first value: "))
num2 = int(input("Enter second value: "))
num3 = int(input("Enter third value: "))

# Computes the sum of the three values and stores it in a variable called sum
sum = num1 + num2 + num3

# Prints the sum
print("The sum is:", sum)

