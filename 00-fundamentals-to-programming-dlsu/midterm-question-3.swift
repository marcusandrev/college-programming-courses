print("Enter your first integer:")
let a = readLine()

print("Enter your second integer:")
let b = readLine()

print("Enter your third integer:")
let c = readLine()

var num1 = Double(a!)
var num2 = Double(b!)
var num3 = Double(c!)

var sum = num1! + num2! + num3!
var prod = num1! * num2! * num3!
var diff = num1! - num2! - num3!
var quo = num1! / num2! / num3!

print("Sum =", sum)
print("Product =", prod)
print("Difference =", diff)
print("Quotient =", quo)



